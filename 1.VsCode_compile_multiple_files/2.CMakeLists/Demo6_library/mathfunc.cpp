#include "mathfunc.h"

int getMax(int a, int b)
{
    return a > b ? a : b;
}
int getMin(int a, int b)
{
    return a < b ? a : b;
}
int plus_(int a, int b)
{
    return a + b;
}
int sub(int a, int b)
{
    return a - b;
}
int mul(int a, int b)
{
    return a * b;
}
int div_(int a, int b)
{
    return a / b;
}